    private void btnBuscarCpfActionPerformed(java.awt.event.ActionEvent evt) {
        String cpfBusca = txtBuscarCPF.getText();

        if (cpfBusca.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um CPF para buscar", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Model.Vendas vendaEncontrada = vendasController.buscarPorCpf(cpfBusca.replaceAll("[^0-9]", ""));

            if (vendaEncontrada != null) {
                // Guarda o ID (que não está na tela, mas é crucial para o Update)
                idVendaAtual = vendaEncontrada.getId();

                txtData.setText(vendaEncontrada.getData());
                txtValor.setText(String.valueOf(vendaEncontrada.getValor()));
                txtCpf.setText(vendaEncontrada.getCliente().getCpf());
                txtNumMatricula.setText(String.valueOf(vendaEncontrada.getFuncionario().getNumMatricula()));
                txtChassi.setText(vendaEncontrada.getVeiculo().getChassi());

                JOptionPane.showMessageDialog(this, "Venda encontrada! Altere os dados e clique em SALVAR.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Venda com CPF " + cpfBusca + " não encontrada!", "Erro", JOptionPane.ERROR_MESSAGE);
                lblLimparActionPerformed(evt);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar venda: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {
        // Se o ID for -1, significa que não houve busca válida
        if (idVendaAtual == -1) {
            JOptionPane.showMessageDialog(this, "Busque uma venda primeiro!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // Cria objeto com dados novos
            Model.Vendas vendaAlterada = new Model.Vendas();
            vendaAlterada.setData(txtData.getText());
            vendaAlterada.setValor(Double.parseDouble(txtValor.getText().replace(",", ".")));

            // Relacionamentos
            Model.Cliente c = new Model.Cliente();
            c.setCpf(txtCpf.getText());
            vendaAlterada.setCliente(c);

            Model.Funcionario f = new Model.Funcionario();
            f.setNumMatricula(Integer.parseInt(txtNumMatricula.getText()));
            vendaAlterada.setFuncionario(f);

            Model.Veiculo v = new Model.Veiculo();
            v.setChassi(txtChassi.getText());
            vendaAlterada.setVeiculo(v);

            // Chama o alterar passando o ID que guardamos
            vendasController.alterar(vendaAlterada, idVendaAtual);

            JOptionPane.showMessageDialog(this, "Venda alterada com sucesso!");

            lblLimparActionPerformed(evt);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valor e Matrícula devem ser numéricos!", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao alterar: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

